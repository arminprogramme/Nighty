function deleteProductBasket(id) {
    fetch(`${mainURL}basket/deleteProduct/${id}`, {
            method: "DELETE",
        })
        .then(res => res.json())
        .then(data => {
            console.log(data);
            getProductBasket()
            location.reload()
        })
    console.log(id);
}